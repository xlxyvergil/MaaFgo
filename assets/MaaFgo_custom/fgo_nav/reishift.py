def reishift(quest, device):
    """
    导航到指定关卡
    
    Args:
        quest: tuple，如 (1, 0, 3, 0) 表示冬木的X-D关卡
        device: FgoDevice实例，提供截图和操作接口
    """
    # 逐级导航
    for i in range(1, len(quest) + 1):
        step = quest[:i]
        if step in place:
            nav = place[step]
            nav(device)
    
    return True
