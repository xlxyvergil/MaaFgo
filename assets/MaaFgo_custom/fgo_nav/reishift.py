class List:
    """列表导航 - 用于章节选择"""
    
    def __init__(self, name):
        self.name = name
    
    def __call__(self, device):
        detect = Detect(device)
        # 等待主界面
        while not detect.isMainInterface():
            time.sleep(0.1)
            detect.refresh()
        
        # 滚动到列表顶部
        while not detect.isQuestListBegin():
            device.swipe((1000, 200), (1000, 600))
            time.sleep(0.4)
            detect.refresh()
        
        # 查找并点击章节
        while True:
            pos = detect.findChapter(self.name)
            if pos:
                device.touch(pos)
                return True
            device.swipe((1000, 600), (1000, 200))
            time.sleep(0.4)
            detect.refresh()


class Map:
    """地图导航 - 用于自由本"""
    
    poly = np.array([
        (230, 40), (230, 200), (40, 200), (40, 450), (150, 450),
        (220, 520), (630, 520), (630, 680), (980, 680), (980, 550),
        (1110, 550), (1110, 360), (1240, 360), (1240, 40)
    ], dtype=np.int32)
    
    def __init__(self, name, coord):
        self.name = name
        self.coord = np.array(coord)
    
    def __call__(self, device):
        detect = Detect(device)
        chapter = self.name[:2] if len(self.name) >= 2 else self.name[:1]
        
        while True:
            camera = detect.findMapCamera(chapter)
            dist = self.coord - camera
            
            # 检查是否在有效点击区域内
            if cv2.pointPolygonTest(self.poly, tuple(self.coord - camera + [640, 360]), False) >= 0:
                device.touch(self.coord)
                return True
            
            # 计算滑动方向和距离
            move = dist / [1422, 800] * [2000, 1200]
            move = np.clip(move, -800, 800)
            device.swipe((640 + move[0] / 2, 360 + move[1] / 2), 
                        (640 - move[0] / 2, 360 - move[1] / 2))
            time.sleep(0.5)
            detect.refresh()


